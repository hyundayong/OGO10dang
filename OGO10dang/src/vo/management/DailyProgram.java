package vo.management;

public class DailyProgram {
	private String[] exercisename = new String[4];
	private String[] quantity = new String[4];
	private String[] link = new String[4];
	private String[] goodfood = new String[2];
	private String[] badfood = new String[2];
	
	
	
	public String[] getExercisename() {
		return exercisename;
	}
	public void setExercisename(String[] exercisename) {
		this.exercisename = exercisename;
	}
	public String[] getQuantity() {
		return quantity;
	}
	public void setQuantity(String[] quantity) {
		this.quantity = quantity;
	}
	public String[] getLink() {
		return link;
	}
	public void setLink(String[] link) {
		this.link = link;
	}
	public String[] getGoodfood() {
		return goodfood;
	}
	public void setGoodfood(String[] goodfood) {
		this.goodfood = goodfood;
	}
	public String[] getBadfood() {
		return badfood;
	}
	public void setBadfood(String[] badfood) {
		this.badfood = badfood;
	}
	
	
	
}
